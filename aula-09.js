// funciton = só executa as variaveis quando elas são chamadas
// return = ele retorna as variaveis que eu escolher

function soma(item1, item2){
    return item1 + item2;
}

function diminuir(item1, item2){
    return item1 - item2;
}

console.log('Soma 1: ', soma(5, 6));
console.log('Soma 2: ', soma(700, 100));
console.log('Soma 3: ', diminuir(900, 300));